# Задача 4

# Назовём число красивым, если оно является четырёхзначным и делится нацело на 7 или на 17. 
# Напишите программу, определяющую, является ли введённое число красивым. Программа должна вывести «YES» (без кавычек), 
# если число является красивым, или «NO» (без кавычек) в противном случае.

numX = int(input("Введите число x: "))

if (1000 <= numX < 10000) and (numX % 7 == 0 or numX % 17 == 0):
    print("YES")
else:
    print("NO")
    
    
