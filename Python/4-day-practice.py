# Напишите программу, которая принимает три положительных числа и определяет, существует ли невырожденный треугольник 
# с такими сторонами.

# a+b>c
# a+c>b
# b+c>a

a, b, c = int(input('Введите значение стороны А: ')), int(input('Введите значение стороны B: ')), int(input('Введите значение стороны C: '))

if (a + b) > c and (a+c) > b and (b+c) > a:
    print("YES")
else:
    print("NO")
    

# Задача 6

# Напишите программу, которая определяет, является ли год с данным номером високосным. 
# Если год является високосным, то выведите «YES», иначе выведите «NO».

# Год является високосным, если его номер кратен 4, но не кратен 100, или если он кратен 400.

ourYear = int(input("Введите год: "))

if (ourYear % 4 == 0 and ourYear % 100 != 0) or ourYear % 400 == 0:
    print("YES")
else:
    print("NO")